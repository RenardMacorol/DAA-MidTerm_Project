
public class Product {
    String name;
    String weight;
    int amount;

    public Product(String name, String weight, int amount) {
        this.name = name;
        this.weight = weight;
        this.amount = amount;
    }

    public String getName() {
        return name;
    }

    public String getWeight() {
        return weight;
    }

    public int getAmount() {
        return amount;
    }

   
}
